package user;

public class User {
    String dosage;
    Integer age;
    String userMedicineName;

    public String getDosage() {
        return dosage;
    }

    public void setDosage(String dosage) {
        this.dosage = dosage;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getUserMedicineName() {
        return userMedicineName;
    }

    public void setUserMedicineName(String userMedicineName) {
        this.userMedicineName = userMedicineName;
    }
}
